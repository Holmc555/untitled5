SELECT c.NAME, COUNT(p.ID) AS PROJECT_COUNT
FROM client c
JOIN project p ON c.ID = p.CLIENT_ID
GROUP BY c.ID, c.NAME
HAVING COUNT(p.ID) = (
    SELECT MAX(PROJECT_COUNT)
    FROM (
        SELECT COUNT(p.ID) AS PROJECT_COUNT
        FROM client c
        JOIN project p ON c.ID = p.CLIENT_ID
        GROUP BY c.ID
    ) AS subquery
)
ORDER BY PROJECT_COUNT DESC;